def adiçao(primeiro_num=0,segundo_num=0):
    somar = primeiro_num + segundo_num
    print(f"{primeiro_num}+{segundo_num}={somar}")

def subitraçao(primeiro_num=0,segundo_num=0):
    subitrair = primeiro_num - segundo_num
    print(f"{primeiro_num}-{segundo_num}={subitrair}")

def mutiplicaçao(primeiro_num=0,segundo_num=0):
    mutiplicar = primeiro_num * segundo_num 
    print(f"{primeiro_num}*{segundo_num}={mutiplicar}")

def divisao(primeiro_num=0,segundo_num=0):
    dividir = primeiro_num / segundo_num
    print(f"{primeiro_num}/{segundo_num}={dividir}")